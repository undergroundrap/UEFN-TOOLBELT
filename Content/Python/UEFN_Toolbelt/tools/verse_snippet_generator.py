SNIPPETS_DIR = os.path.join(
    unreal.Paths.project_saved_dir(), "UEFN_Toolbelt", "snippets"
)

# Subcategory folders — every snippet goes into one of these
SUBCAT_GAME_SYSTEMS  = "game_systems"    # game manager, round logic, scoring
SUBCAT_DEVICE_WIRING = "device_wiring"  # device declarations, prop spawner
SUBCAT_SPLINE_TOOLS  = "spline_tools"   # spline → Verse converters
SUBCAT_CUSTOM        = "custom"         # user-generated / Claude-generated

# ─────────────────────────────────────────────────────────────────────────────
#  Helpers
# ─────────────────────────────────────────────────────────────────────────────

def _write_snippet(filename: str, content: str, subcategory: str = SUBCAT_CUSTOM) -> str:
    """Write snippet to disk under the appropriate subcategory folder."""
    dest = os.path.join(SNIPPETS_DIR, subcategory)
    os.makedirs(dest, exist_ok=True)
    path = os.path.join(dest, filename)
    with open(path, "w") as f:
        f.write(content)
    return path


def _try_clipboard(text: str) -> None:
    """Copy text to Windows clipboard if available."""
    try:
        import subprocess
        subprocess.run(["clip"], input=text.encode("utf-8"), check=True)
        log_info("  Snippet copied to clipboard.")
    except Exception:
        pass  # Not on Windows, or clip not available


def _actor_to_verse_device_type(actor: unreal.Actor) -> str:
    """
    Map a UE actor class name to the nearest Verse device type.
    These are approximations — adjust to match your exact UEFN SDK version.
    """
    class_name = actor.get_class().get_name().lower()
    mapping = {
        "trigger":       "trigger_device",
        "timer":         "timer_device",
        "spawnpad":      "player_spawner_device",
        "button":        "button_device",
        "chest":         "chest_device",
        "beacon":        "capture_area_device",
        "zone":          "mutator_zone_device",
        "tracker":       "tracker_device",
        "scoreboard":    "scoreboard_device",
        "barrier":       "barrier_device",
        "ammo":          "item_spawner_device",
        "vehicle":       "vehicle_spawner_device",
        "elimination":   "elimination_manager_device",
    }
    for key, verse_type in mapping.items():
        if key in class_name:
            return verse_type
    return "creative_device"  # fallback base type


def _safe_ident(label: str) -> str:
    """Convert actor label to a valid Verse identifier."""
    result = label.strip().replace(" ", "_").replace("-", "_")
    result = "".join(c if c.isalnum() or c == "_" else "_" for c in result)
    if result and result[0].isdigit():
        result = "d_" + result
    return result.lower()



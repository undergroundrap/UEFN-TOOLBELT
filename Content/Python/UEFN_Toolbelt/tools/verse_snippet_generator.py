def _find_uefn_project_root() -> str:
    """
    Walk up from this file's location to find the UEFN project root.
    The Toolbelt lives at [ProjectRoot]/Content/Python/UEFN_Toolbelt/
    so we walk up until we find the 'Content' directory, then return its parent.
    This is the only reliable way to find the user project root in UEFN —
    unreal.Paths.project_dir() returns the FortniteGame engine directory, not
    the user project directory.
    """
    curr = os.path.abspath(__file__)
    while True:
        parent = os.path.dirname(curr)
        if parent == curr:
            # Reached filesystem root without finding Content — fall back
            return unreal.Paths.project_saved_dir()
        if os.path.basename(curr) == "Content":
            return parent
        curr = parent


def _find_verse_project_dir() -> str:
    """
    Auto-detect the Verse source directory for the current UEFN project.

    Search order:
    1. config verse.project_path (user override — most reliable)
    2. [ProjectRoot]/Verse/  (standard UEFN layout)
    3. [ProjectRoot]/*.verse/ (Epic's Verse package folder naming)
    4. Falls back to Saved/UEFN_Toolbelt/snippets/custom/ with a clear warning

    NOTE: Never uses unreal.Paths.project_dir() — in UEFN that resolves to
    the FortniteGame engine directory, not the user's project directory.
    """
    from ..core.config import get_config
    cfg = get_config()
    user_path = cfg.get("verse.project_path", "")
    if user_path and os.path.isdir(user_path):
        return user_path

    project_root = _find_uefn_project_root()

    # Standard layout
    standard = os.path.join(project_root, "Verse")
    if os.path.isdir(standard):
        return standard

    # Epic's .verse package folder naming
    for entry in os.listdir(project_root):
        full = os.path.join(project_root, entry)
        if entry.endswith(".verse") and os.path.isdir(full):
            return full

    # Fallback — create standard Verse dir in project root
    os.makedirs(standard, exist_ok=True)
    return standard


@register_tool(
    name="verse_find_project_path",
    category="Verse Helpers",
    description="Auto-detect and return the Verse source directory for the current UEFN project.",
    tags=["verse", "path", "project", "config"],
)
def run_verse_find_project_path(**kwargs) -> dict:
    """
    Returns the path Claude should use when writing .verse files for this project.
    Set config verse.project_path to override auto-detection.

    Returns:
        {"status": "ok", "path": str, "source": "config"|"standard"|"package"|"created"}
    """
    from ..core.config import get_config
    cfg = get_config()
    user_path = cfg.get("verse.project_path", "")
    if user_path and os.path.isdir(user_path):
        log_info(f"Verse project path (from config): {user_path}")
        return {"status": "ok", "path": user_path, "source": "config"}

    project_root = _find_uefn_project_root()
    standard = os.path.join(project_root, "Verse")
    if os.path.isdir(standard):
        log_info(f"Verse project path (standard): {standard}")
        return {"status": "ok", "path": standard, "source": "standard"}

    for entry in os.listdir(project_root):
        full = os.path.join(project_root, entry)
        if entry.endswith(".verse") and os.path.isdir(full):
            log_info(f"Verse project path (package): {full}")
            return {"status": "ok", "path": full, "source": "package"}

    os.makedirs(standard, exist_ok=True)
    log_info(f"Verse project path (created): {standard}")
    return {"status": "ok", "path": standard, "source": "created"}


@register_tool(
    name="verse_write_file",
    category="Verse Helpers",
    description=(
        "Write Verse code directly into the project's Verse source directory — "
        "the file is immediately visible to UEFN's compiler. "
        "The AI deployment layer: Claude generates Verse, this tool places it."
    ),
    tags=["verse", "write", "file", "deploy", "ai", "automation", "compile"],
    example='tb.run("verse_write_file", filename="game_manager.verse", content=verse_code, overwrite=True)',
)
def run_verse_write_file(
    filename: str = "",
    content: str = "",
    subdir: str = "",
    overwrite: bool = False,
    **kwargs,
) -> dict:
    """
    Write ``content`` to ``filename`` inside the project's Verse source directory.
    After writing, open UEFN and click 'Compile Verse' (or run system_build_verse).

    Args:
        filename:  The .verse filename (e.g. "game_manager.verse").
                   A .verse extension is added automatically if missing.
        content:   The full Verse source code to write.
        subdir:    Optional subdirectory inside the Verse project root.
        overwrite: If False (default), raises an error if the file already exists
                   so Claude never silently stomps existing work.

    Returns:
        {
          "status": "ok",
          "path": str,           # absolute path of the written file
          "bytes": int,
          "next_step": str       # instruction for what to do next
        }

    Example:
        tb.run("verse_write_file",
               filename="game_manager.verse",
               content="game_manager := class(creative_device): ...",
               overwrite=True)
    """
    if not filename:
        return {"status": "error", "message": "filename is required."}
    if not content:
        return {"status": "error", "message": "content is required."}

    if not filename.endswith(".verse"):
        filename += ".verse"

    verse_root = _find_verse_project_dir()
    dest_dir = os.path.join(verse_root, subdir) if subdir else verse_root
    os.makedirs(dest_dir, exist_ok=True)
    out_path = os.path.join(dest_dir, filename)

    if os.path.exists(out_path) and not overwrite:
        return {
            "status": "error",
            "message": f"File already exists: {out_path}. Pass overwrite=True to replace.",
            "path": out_path,
        }

    with open(out_path, "w", encoding="utf-8") as f:
        f.write(content)

    log_info(f"[verse_write_file] ✓ Written: {out_path} ({len(content)} bytes)")
    return {
        "status": "ok",
        "path": out_path,
        "bytes": len(content),
        "next_step": "Click 'Compile Verse' in UEFN, or run tb.run('system_build_verse') to compile and check for errors.",
    }


@register_tool(
    name="verse_gen_custom",
    category="Verse Helpers",
    description=(
        "Write a custom Verse snippet to disk. Pass spec-accurate code generated "
        "by Claude after querying verse_book_search / verse_book_chapter."
    ),
    tags=["verse", "snippet", "custom", "generate", "ai", "spec"],
)
def run_gen_custom(
    filename: str = "custom_snippet.verse",
    code: str = "",
    description: str = "",
    **kwargs,
) -> dict:
    """
    Write arbitrary Verse code to the snippets folder.
    Intended to be called by Claude after consulting the verse-book spec
    via verse_book_search / verse_book_chapter MCP tools.

    Args:
        filename:    Output filename (e.g. 'my_device.verse').
        code:        The Verse code to write (spec-verified by Claude).
        description: Human-readable description added as a header comment.
    """
    if not code:
        log_warning("verse_gen_custom: no code provided.")
        return {"path": "", "bytes": 0}

    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    header = (
        f"# ============================================================\n"
        f"# Generated by UEFN Toolbelt — verse_gen_custom\n"
        f"# {timestamp}\n"
    )
    if description:
        header += f"# {description}\n"
    header += "# Spec: https://github.com/verselang/book\n"
    header += "# ============================================================\n\n"

    content = header + code
    if not filename.endswith(".verse"):
        filename += ".verse"
    path = _write_snippet(filename, content, SUBCAT_CUSTOM)
    _try_clipboard(content)
    log_info(f"Custom Verse snippet written → {path}")
    return {"path": path, "bytes": len(content)}


@register_tool(
    name="verse_list_snippets",
    category="Verse Helpers",
    description="List all generated snippet files in the Saved/UEFN_Toolbelt/snippets/ folder.",
    tags=["verse", "snippet", "list"],
)
def run_list_snippets(**kwargs) -> dict:
    """
    Returns:
        dict: {"status", "count", "snippets": [{"subcat", "filename", "size_bytes"}]}
    """
    if not os.path.isdir(SNIPPETS_DIR):
        log_info("No snippets generated yet. Run a verse_gen_* tool first.")
        return {"status": "ok", "count": 0, "snippets": []}
    lines = [f"\n=== Verse Snippet Library ({SNIPPETS_DIR}) ==="]
    all_snippets = []
    for subcat in sorted(os.listdir(SNIPPETS_DIR)):
        subdir = os.path.join(SNIPPETS_DIR, subcat)
        if not os.path.isdir(subdir):
            continue
        files = [f for f in sorted(os.listdir(subdir)) if f.endswith(".verse")]
        if not files:
            continue
        lines.append(f"\n  [{subcat}]")
        for f in files:
            size = os.path.getsize(os.path.join(subdir, f))
            lines.append(f"    {f:44s}  {size:>6} bytes")
            all_snippets.append({"subcat": subcat, "filename": f, "size_bytes": size})
    lines.append(f"\n  {len(all_snippets)} snippet(s) total")
    log_info("\n".join(lines))
    return {"status": "ok", "count": len(all_snippets), "snippets": all_snippets}


